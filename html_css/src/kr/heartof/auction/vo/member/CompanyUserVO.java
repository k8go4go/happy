package kr.heartof.auction.vo.member;

public class CompanyUserVO extends UsrVO{
	private String COM_NM ;
	private String BUS_NO ;
	private String CEO_NM ;
	private String CHG_NM ;
	/**
	 * @return the cOM_NM
	 */
	public String getCOM_NM() {
		return COM_NM;
	}
	/**
	 * @param cOM_NM the cOM_NM to set
	 */
	public void setCOM_NM(String cOM_NM) {
		COM_NM = cOM_NM;
	}
	/**
	 * @return the bUS_NO
	 */
	public String getBUS_NO() {
		return BUS_NO;
	}
	/**
	 * @param bUS_NO the bUS_NO to set
	 */
	public void setBUS_NO(String bUS_NO) {
		BUS_NO = bUS_NO;
	}
	/**
	 * @return the cEO_NM
	 */
	public String getCEO_NM() {
		return CEO_NM;
	}
	/**
	 * @param cEO_NM the cEO_NM to set
	 */
	public void setCEO_NM(String cEO_NM) {
		CEO_NM = cEO_NM;
	}
	/**
	 * @return the cHG_NM
	 */
	public String getCHG_NM() {
		return CHG_NM;
	}
	/**
	 * @param cHG_NM the cHG_NM to set
	 */
	public void setCHG_NM(String cHG_NM) {
		CHG_NM = cHG_NM;
	}
}
